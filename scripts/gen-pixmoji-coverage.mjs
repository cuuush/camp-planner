// Build the set of emoji codepoints that UnifontEX ACTUALLY has a glyph for, so
// the client only pixel-ifies those and leaves the rest as native (color) emoji
// instead of UnifontEX's ugly hex-box tofu (camera, mirror, newer emoji, …).
//
// Input: the repo's glyph list (one big string of every character the font
// covers). Output: src/render/pixmoji-coverage.js exporting compact ascending
// [start,end,start,end,…] ranges of covered Extended_Pictographic / flag codepoints.
import fs from 'node:fs';

const SCRATCH = '/private/tmp/claude-501/-Users-chris-code-camp-planner/cb9983fa-3bbd-44bb-8aa1-2de5f9d56c93/scratchpad';
const GLYPHS = `${SCRATCH}/glyphs.txt`;      // UnifontEX (BMP + Unicode <= 11)
const UPPER_HEX = `${SCRATCH}/up.hex`;        // GNU Unifont 16 upper plane (has the newer emoji)
const OUT = '/Users/chris/code/camp-planner/src/render/pixmoji-coverage.js';

const covered = new Set();
// UnifontEX coverage (the 2MB base woff2).
for (const ch of fs.readFileSync(GLYPHS, 'utf8')) covered.add(ch.codePointAt(0));
// Plus the Unifont-16 emoji subset we ship as a fallback woff2: the whole
// U+1F000–1FAFF block it provides glyphs for (mirror, wood, chair, 2019+ emoji).
for (const line of fs.readFileSync(UPPER_HEX, 'utf8').split('\n')) {
    const key = line.split(':')[0];
    if (!key) continue;
    const cp = parseInt(key, 16);
    if (cp >= 0x1f000 && cp <= 0x1faff) covered.add(cp);
}

// The codepoints pixmojify can match: Extended_Pictographic plus regional
// indicators (flags). Only these ever need a coverage check.
const isCandidate = (cp) => {
    const ch = String.fromCodePoint(cp);
    if (/\p{Extended_Pictographic}/u.test(ch)) return true;
    if (cp >= 0x1f1e6 && cp <= 0x1f1ff) return true; // regional indicators
    return false;
};

const coveredCps = [];
let total = 0;
const missingSample = [];
for (let cp = 0x2000; cp <= 0x1faff; cp++) {
    if (!isCandidate(cp)) continue;
    total++;
    if (covered.has(cp)) coveredCps.push(cp);
    else if (missingSample.length < 40) missingSample.push(cp.toString(16).toUpperCase());
}

// Compress covered codepoints into [start,end] ranges.
const ranges = [];
for (const cp of coveredCps) {
    const n = ranges.length;
    if (n && ranges[n - 1] === cp - 1) ranges[n - 1] = cp;
    else ranges.push(cp, cp);
}

const banner = `// AUTO-GENERATED by scripts/gen-pixmoji-coverage.mjs — do not edit by hand.\n`
    + `// Ascending [start,end,start,end,…] codepoint ranges that UnifontEX covers\n`
    + `// among emoji/flag codepoints. Used so we don't pixel-ify tofu glyphs.\n`;
fs.writeFileSync(OUT, `${banner}export const PIXMOJI_COVERED_RANGES = [${ranges.join(',')}];\n`);

console.log(`candidate emoji codepoints: ${total}`);
console.log(`covered by UnifontEX:       ${coveredCps.length}`);
console.log(`missing (not pixel-ified):  ${total - coveredCps.length}`);
console.log(`ranges emitted:             ${ranges.length / 2}`);
console.log(`camera 1F4F7 covered? ${covered.has(0x1f4f7)}   mirror 1FA9E covered? ${covered.has(0x1fa9e)}`);
console.log(`sample missing: ${missingSample.join(' ')}`);
